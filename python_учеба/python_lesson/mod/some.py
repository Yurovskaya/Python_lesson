def sum(a, b):
    result = a + b
    print(f"Сумма переменных равна: {result}")

def sub(a, b):
    result = a - b
    print(f"Разница переменных равна: {result}")

def multiplication (a , b):
    result = a * b
    print(f"Результат умножения: {result}")

def divided (a, b):
    result = a / b
    print(f"Результат деления: {result}")